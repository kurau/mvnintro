package ru.kurau.exp;

/**
 * @author kurau (Yuri Kalinin)
 */
public class Calculator {

    public long adding(int a, int b) {
        return a + b;
    }

    public long subtracting(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

}
